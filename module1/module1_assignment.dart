void main() {
  const int a = 19;
  const int b = 12;

  print('a = $a, b = $b');

  // Addition
  print('Addition: $a + $b = ${a + b}');

  // Subtraction
  print('Subtraction: $a - $b = ${a - b}');

  // Multiplication
  print('Multiplication: $a * $b = ${a * b}');

  // Division
  print('Division: $a / $b = ${a / b}');

  // Modulus
  print('Modulus: $a % $b = ${a % b}');
}
